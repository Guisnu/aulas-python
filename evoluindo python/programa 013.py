#Estrutura condicional parte 2

idade = int(input("Digite a idade: "))

if(idade >= 18):
    print("Essa pessoa é maior de idade")
else:
    print("Essa pessoa é menor de idade")

#se(if) a idade for maior ou igual a 18 será executado "Essa pessoa é maior de idade"

#se não(else) será executado "Essa pessoa é menor de idade"